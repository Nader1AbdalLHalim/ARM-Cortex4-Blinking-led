/**********************************************************************************************************************
 *  LOCAL DATA
 *********************************************************************************************************************/
/* Global variable to save Time On */
static  volatile uint32 CTime_on = 5;    /*5 SECOND*/

/* Global variable to save Time Off */
static  volatile uint32 CTime_off = 2;   /*1 Second*/

/**********************************************************************************************************************
 *  GLOBAL DATA
 *********************************************************************************************************************/

/**********************************************************************************************************************
 *  LOCAL FUNCTION PROTOTYPES
 *********************************************************************************************************************/

/**********************************************************************************************************************
 *  LOCAL FUNCTIONS
 *********************************************************************************************************************/

/**********************************************************************************************************************
 *  GLOBAL FUNCTIONS
 *********************************************************************************************************************/

uint32 Time_GetOn(void)
{
	return CTime_on ;
}

uint32 Time_GetOff(void)
{
	return CTime_off ;
}

/*Function to Calculate Timer Ticks.*/
uint32 Time_GetTimerTicks(uint32 TimeinSeconds)
{
	float Frequency = F_CPU / 4 ;

	float OneTickTime = (float)1 / Frequency ;

	float TotalTicks  =(float) TimeinSeconds / OneTickTime  ;

	return  (uint32)TotalTicks ;
}
